// ─── Platform definitions ─────────────────────────────────────────────────────

export type AdPlatform =
  | "instagram"
  | "tiktok"
  | "facebook"
  | "x_twitter"
  | "linkedin"
  | "youtube_shorts"
  | "pinterest";

export type ContentFormat = "image_post" | "video_reel" | "story" | "carousel" | "banner_ad";

export interface ContentBrief {
  platform: AdPlatform;
  format: ContentFormat;
  theme: string;           // e.g. "AI film production", "cinematic storytelling"
  hook: string;            // opening line / attention grabber
  caption: string;         // full post caption
  hashtags: string[];
  imagePrompt: string;     // detailed prompt for image generation
  videoPrompt: string;     // detailed prompt for video generation
  cta: string;             // call to action
  aspectRatio: "landscape" | "portrait";
}

export interface ContentCreatorResult {
  success: boolean;
  contentId?: number;
  imageUrl?: string;
  videoUrl?: string;
  caption?: string;
  hashtags?: string[];
  platform: AdPlatform;
  error?: string;
}

